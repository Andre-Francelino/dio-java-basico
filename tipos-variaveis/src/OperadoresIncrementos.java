public class OperadoresIncrementos {
    public static void main(String[] args) {
        int numero = 5;

        //numero = numero + 1;
        System.out.println(numero ++);
        System.out.println(numero);

        //numero = numero - 1;
        System.out.println(numero --);
        System.out.println(numero);
    }
}
