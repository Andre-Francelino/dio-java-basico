public class OperadoresUnarios {
    public static void main(String[] args) {

        int numero = 5;
        numero = - numero;
        System.out.println(numero);

        //numero = + numero;
    // o + é um operador de adição, para somar. Para converter para positivo é necessário usar a multiplicação *
        numero = numero * -1;
        System.out.println(numero);

    }
}
